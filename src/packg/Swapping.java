package packg;

public class Swapping {

	public static void main(String[] args) {
		// swapping
		int a=20 ,b=30,c;//using variable c
System.out.println("value before swapping"+a+"\n"+"values before swapping"+b);
c=a;//c=20
a=b;//a=30
b=c;//b=20
System.out.println("values after swapping"+a+"\n"+"values after swapping"+b);
	}

}
